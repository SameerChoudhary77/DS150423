# Slicing - fetching the substring out of original string
# [start_range : end_range : step]

name = "Python Programming Language"

print(name[2:6])

print(name[2:])

print(name[:4])

print(name[:])

print(name[-2:])

print(name[1:6:2])

print(name[::-1])

